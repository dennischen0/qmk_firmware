static void render_bongo(void) {

    //assumes 1 frame prep stage
    void animation_phase(void) {
        if(get_current_wpm() <=IDLE_SPEED){
            /*
            current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
            oled_write_raw_P(idle[abs((IDLE_FRAMES-1)-current_idle_frame)], ANIM_SIZE);
            */
            oled_write_compressed_P(idle_block_x_map, idle_block_x_list, 0, BONGO_ANIM_SIZE);
         }
         /*
         if(get_current_wpm() >IDLE_SPEED && get_current_wpm() <TAP_SPEED){
             // oled_write_raw_P(prep[abs((PREP_FRAMES-1)-current_prep_frame)], ANIM_SIZE); // uncomment if IDLE_FRAMES >1
             oled_write_raw_P(prep[0], ANIM_SIZE);  // remove if IDLE_FRAMES >1
         }*/
         if(get_current_wpm() >=TAP_SPEED){
            current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
            if(abs((TAP_FRAMES-1)-current_tap_frame) == 0) {
                oled_write_compressed_P(tap_1_block_x_map, tap_1_block_x_list, 0, BONGO_ANIM_SIZE);
            } else {
                oled_write_compressed_P(tap_2_block_x_map, tap_2_block_x_list, 0, BONGO_ANIM_SIZE);
            }
         }
    }

    if(timer_elapsed32(bongo_anim_timer) > BONGO_ANIM_FRAME_DURATION) {
        bongo_anim_timer = timer_read32();
        animation_phase();
    }
}

